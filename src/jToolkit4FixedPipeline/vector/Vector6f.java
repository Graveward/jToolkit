package jToolkit4FixedPipeline.vector;

/**
 *
 * @author Astemir Yeleev
 */
public class Vector6f {
    public float x;
    public float y; 
    public float z;
    public float nx;
    public float ny;
    public float nz;

    public Vector6f() {
    }

    public Vector6f(float x, float y, float z, float nx, float ny, float nz) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.nx = nx;
        this.ny = ny;
        this.nz = nz;
    }
    
}
